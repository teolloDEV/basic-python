# Menambah data di belakang
# append ()

list_buah = ['Jeruk', 'Naga', 'Pepaya', 'Mangga']
print(list_buah)
list_buah.append('semangka')
print('\nsetelah data di tambah\n')
print(list_buah)

# Menambah data di depan
# insert (index, nilai)

print('menambah data di depan dengan insert ()')
list_buah.insert(0, 'anggur')
print('setelah ditambah')
print(list_buah)
